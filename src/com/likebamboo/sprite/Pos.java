
package com.likebamboo.sprite;

/**
 * @author likebamboo
 */
public class Pos {

    public float x;

    public float y;

    public Pos(float i, float j) {
        x = i;
        y = j;
    }

    @Override
    public String toString() {
        return "Pos [x=" + x + ", y=" + y + "]";
    }

}
